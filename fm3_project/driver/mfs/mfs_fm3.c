MFS_CallbackT g_pMFSIRQCallback[MFS_MAX_CH*3] = {(void*)0};
/*---------------------------------------------------------------------------*/
/* global functions                                                          */
/*---------------------------------------------------------------------------*/

/*!
 ******************************************************************************
 ** \brief MFS RX Handler.
 **
 ** \param Ch MFS interrupt channel 
 ** \arg   MFS_IntChxRX 
 ** \arg   MFS_MFS_IntChxTX
 ** \arg   MFS_MFS_IntChxStatus
 **
 ** \retval None
 **
 ******************************************************************************
 */
void MFS_RX_IntHandler(uint8_t Ch)
{
    if(g_pMFSIRQCallback[Ch*3] != NULL)
    {
        g_pMFSIRQCallback[Ch*3]();
    }
}
/*!
 ******************************************************************************
 ** \brief MFS TX Handler.
 **
 ** \param None        
 **
 ** \retval None
 **
 ******************************************************************************
 */
void MFS_TX_IntHandler(uint8_t Ch, uint8_t IntType)
{
    if((IntType & 0x01) == 0x01)
    {
        if(g_pMFSIRQCallback[Ch*3+1] != NULL)
        {
            g_pMFSIRQCallback[Ch*3+1]();
        }
    }
    
    if((IntType & 0x02) == 0x02)
    {
        if(g_pMFSIRQCallback[Ch*3+1] != NULL)
        {
            g_pMFSIRQCallback[Ch*3+2]();
        }
    }
      
}

